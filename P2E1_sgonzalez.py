#Hacer un diagrama para calcular y mostrar el área de un triángulo



print ("Vamos a calcular el área de un triángulo")
base=float(input("Dime cuánto mide la base del triángulo "))
altura=float(input("Dime cuánto mide la altura del triángulo "))
area=((base*altura)/2)
print (area)
print ("El area del triángulo mide", area, "unidades cuadradas")